package Java;
/*Codigo operadores y variables */
public class variables {
  public static void main(String args[]){
    //!Este es un comentario JAVA
    
    int a=5,b=7;
    int c = b + a;

    c++; //*Incrementa en uno el valor de c
    System.out.println(c);
    c+=3; //*Se incrementa en 3
    System.out.println(c);
    c--;//*Disminuye en 1

    final int d = 4; //*Esta es una constante

    System.out.println(d);

  }
}
